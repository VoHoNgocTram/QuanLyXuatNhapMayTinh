    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
         NguoiDungDTO nv = parent.getNhanVienSelect(); // Lấy NV đang chỉnh sửa

        int selectedIndex = cbbChucVu.getSelectedIndex();
        if (selectedIndex >= 0 && selectedIndex < dsChucVu.size()) {
            int maChucVu = dsChucVu.get(selectedIndex).getMaChucVu();
            int luongMoi = dsChucVu.get(selectedIndex).getLuongCoBan();

            nv.setMaChucVu(maChucVu); // Gán lại mã chức vụ mới cho nhân viên
            nv.setLuongCoBan(luongMoi); // Gán lại lương theo chức vụ mới
        }

        String result = ndBUS.updateChucVuNhanVien(nv);
        JOptionPane.showMessageDialog(this, result);

        if (result.equals("Cập nhật thành công!")) {
            parent.loadDataToTable(); // Load lại dữ liệu trong bảng
            this.dispose(); // Đóng dialog sau khi cập nhật
        }
    }//GEN-LAST:event_btnLuuActionPerformed

