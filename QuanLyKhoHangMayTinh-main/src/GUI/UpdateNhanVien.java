    private void displayInfo() {
        NguoiDungDTO nv = parent.getNhanVienSelect();
        if (nv != null) {
            // Hiển thị họ tên
            txtHoten.setText(nv.getHoTen());

            // Hiển thị chức vụ
            for (int i = 0; i < dsChucVu.size(); i++) {
                if (dsChucVu.get(i).getMaChucVu() == nv.getMaChucVu()) {
                    cbbChucVu.setSelectedIndex(i);
                    break;
                }
            }

            // Hiển thị lương hiện tại của nhân viên
            txtLuongCB.setText(String.valueOf(nv.getLuongCoBan()));
        }
    }

    
    private void loadChucVuToComboBox() {
        cbbChucVu.removeAllItems();
        for (ChucVuDTO chucVu : dsChucVu) {
            cbbChucVu.addItem(chucVu.getTenChucVu());
        }
    }

