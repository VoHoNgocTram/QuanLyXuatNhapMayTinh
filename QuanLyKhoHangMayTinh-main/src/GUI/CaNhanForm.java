    private void authorizeAction(NguoiDungDTO user) {
        // Get all allowed actions in this functionality
        List<ChiTietQuyenDTO> allowedActions = new ArrayList<>();
        try {
            allowedActions = ctqBUS.getAllowedActions(user.getMaNhomQuyen(), "canhan");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(CaNhanForm.this, "Lỗi kết nối cơ sở dữ liệu", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(CaNhanForm.this, "Lỗi không xác định", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return;
        }
        
        for (ChiTietQuyenDTO ctq : allowedActions) {
            
            if (ctq.getHanhDong().equals("update")) {
                btnEdit.setEnabled(true);
                continue;
            }
        }
        
    }
    
    private void loadThongTinCaNhan() throws SQLException {
    // Gọi DAO để lấy dữ liệu từ CSDL
    this.user = NguoiDungDAO.getInstance().getInfoByAccount(taiKhoan);
    if (user != null) {
        taiKhoan = user.getTaiKhoan();
        txtHoTen.setText(user.getHoTen());
        txtEmail.setText(user.getEmail());
        if (user.getNgaySinh() != null) {
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        txtNgaySinh.setText(sdf.format(user.getNgaySinh()));
        } else {
            txtNgaySinh.setText(""); // hoặc "Chưa cập nhật"
        }
        cbbGioiTinh.setSelectedItem(user.getGioiTinh());
        txtDiaChi.setText(user.getDiaChi());
        txtSdt.setText(user.getSdt());
        txtChucVu.setText(user.getTenChucvu());
        txtSoNgayPhep.setText(String.valueOf(user.getSoNgayPhep()));
        txtLuongCB.setText(String.valueOf(user.getLuongCoBan()));
        if (user.getNgayVaoLam() != null) {
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        txtNgayVaoLam.setText(sdf.format(user.getNgayVaoLam()));
        } else {
            txtNgayVaoLam.setText(""); // hoặc "Chưa cập nhật"
        }
    }
}
   
