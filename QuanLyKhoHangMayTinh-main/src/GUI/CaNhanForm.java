    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

        try {
            // Tạo đối tượng NguoiDungDTO
            NguoiDungDTO nd = user;
            String email = txtEmail.getText().trim();
            String ngaySinh = txtNgaySinh.getText().trim();
            String gioiTinh = cbbGioiTinh.getSelectedItem().toString();
            String diaChi = txtDiaChi.getText().trim();
            String sdt = txtSdt.getText().trim();// Giới tính từ ComboBox
            nd.setEmail(email);
            nd.setGioiTinh(gioiTinh);
            nd.setDiaChi(diaChi);
            nd.setSdt(sdt);
            if (email.equals("") || ngaySinh.equals("") || gioiTinh.equals("") || diaChi.equals("") || sdt.equals("")) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin !", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            }
            else if(!sdt.matches("\\d{10}") ){
                JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            }
            else if (!email.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$")) {
                JOptionPane.showMessageDialog(this, "Email không hợp lệ!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            }
            else if (!ngaySinh.matches("\\d{2}-\\d{2}-\\d{4}")) {
                JOptionPane.showMessageDialog(this, "Ngày sinh không hợp lệ! Phải theo định dạng dd-mm-yyyy", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            }
            else if (NguoiDungDAO.getInstance().hasSDTException(nd)) {
                JOptionPane.showMessageDialog(this, "Số điện thoại bị trùng!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            }
            else if (NguoiDungDAO.getInstance().hasEmailException(nd)) {
                JOptionPane.showMessageDialog(this, "Email bị trùng!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(this, ndBUS.updateCaNhan(nd));
                loadThongTinCaNhan();
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnEditActionPerformed

