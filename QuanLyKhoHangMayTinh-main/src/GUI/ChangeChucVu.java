    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        
        String Chucvuhientai = (String) cbxChucVu.getSelectedItem();
        if (!Chucvuhientai.equals(Chucvubandau)) {
            int malscv = Integer.valueOf(txtLscv.getText());
            String taikhoan = mataikhoan;
            String hoten = txtHoten.getText();
            int luong = Integer.valueOf(txtLuong.getText());
            String Chucvu = (String) cbxChucVu.getSelectedItem();
            String[] parts = Chucvu.split(" - ");
            int maChucVu = Integer.parseInt(parts[0]);
            String tenchucvu = parts[1];
            LocalDate Ngaythaydoi = LocalDate.now();
            Date ngayThayDoiDate = Date.from(Ngaythaydoi.atStartOfDay(ZoneId.systemDefault()).toInstant());
            int Trangthai = 1;
            
            danhSachNhomQuyen = nqBUS.getAllDSNhomQuyen();
            
            int manhomquyen = -1; // Khởi tạo giá trị mặc định
            for (NhomQuyenDTO i : danhSachNhomQuyen) {
                if (i.getTenNhomQuyen().equals(tenchucvu)) {
                    manhomquyen = i.getMaNhomQuyen();
                    break; // Thoát vòng lặp khi tìm thấy
                }
            }

            if (manhomquyen == -1) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy nhóm quyền phù hợp với chức vụ");
                return; // Không tiếp tục nếu không có nhóm quyền phù hợp
            }
            lsChucVuDTO lscv = new lsChucVuDTO(malscv, taikhoan, hoten, maChucVu, tenchucvu, ngayThayDoiDate, luong, Trangthai);

            boolean result = nvBUS.capNhatChucVuNguoiDung(mataikhoan, maChucVu, tenchucvu, manhomquyen, ngayThayDoiDate, lscv);
            if (result) {
                JOptionPane.showMessageDialog(this, "Thay đổi chức vụ thành công");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Thay đổi thất bại");
            }
            
        }else {
            JOptionPane.showMessageDialog(this, "Chức vụ không thay đổi");
            dispose();
        }
        
        
    }//GEN-LAST:event_btnLuuActionPerformed

