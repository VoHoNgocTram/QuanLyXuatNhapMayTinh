    public ChangeChucVu(NguoiDungDTO nv) {
        initComponents();
        initTable();
        // Lưu danh sách chức vụ vào biến toàn cục
        danhSachChucVu = nvBUS.getAllDSChucVu();
        txtLscv.setText(""); // có thể tự sinh
        lsChucVu = nvBUS.getAllLichSuChucVu();
        int maCuoi = lsChucVu.get(lsChucVu.size() - 1).getMals(); 
        int maMoi = maCuoi + 1;
        String mals = String.valueOf(maMoi);
        txtLscv.setText(mals);
        txtLscv.setEditable(false);
        mataikhoan = nv.getTaiKhoan();
        
        nv = nvBUS.getNhanVienByTaiKhoan(mataikhoan);
        
        txtHoten.setText(nv.getHoTen());
        txtHoten.setEditable(false);
        txtLuong.setText(String.valueOf(nv.getLuongCoBan())); 
        txtLuong.setEditable(false);
        // và các thao tác khác tùy ý
        // Load danh sách chức vụ
        ArrayList<ChucVuDTO> listCV = nvBUS.getAllDSChucVu();  // hoặc hàm tương đương
        for (ChucVuDTO cv : listCV) {
            cbxChucVu.addItem(cv.getMaChucVu()+" - "+cv.getTenChucVu());
        }
        // Gán sự kiện chọn chức vụ để cập nhật lương tương ứng
        cbxChucVu.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    int selectedIndex = cbxChucVu.getSelectedIndex();
                    if (selectedIndex >= 0 && selectedIndex < danhSachChucVu.size()) {
                        int luong = danhSachChucVu.get(selectedIndex).getLuongCoBan();
                        txtLuong.removeAll();
                        txtLuong.setText(String.valueOf(luong));
                        txtLuong.setEditable(false);
                    }
                }
            }
        });
        // Set chức vụ hiện tại đang có của nhân viên (nếu cần gợi ý)
        cbxChucVu.setSelectedItem(nv.getMaChucVu()+" - "+nv.getTenChucvu());
        Chucvubandau = nv.getMaChucVu()+"-"+nv.getTenChucvu();
        loadDataToTable(mataikhoan);
        
        
    }
    
